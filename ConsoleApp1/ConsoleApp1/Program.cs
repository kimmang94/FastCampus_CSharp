namespace HelloWorld
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello, World!");
        }
    }

    // using : 선언된 네임스페이스 내부 개체들에 접근
    // namespace : 그룹화, 동일한 함수의 이름 사용 가능
    // class : 필드(field), 메소드로(method)로 분리, 프로그램을 구성하는 기본
    // 세미콜론 ';' : 코드 문장의 끝
    // 컬리 브레이스 '{}' : 코드블록(블록 단위로 코드 구분)
    // 주석 : // 한줄주석 , /* */ 여러줄 주석

    /*
     * static void Main 함수
     * 가장 중요한 메소드
     * 최초의 진입점 (Entry Point)
     * 메인 메소드가 반드시 존재 
     */
}
